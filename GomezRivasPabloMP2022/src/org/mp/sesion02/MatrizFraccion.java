package org.mp.sesion02;

import org.mp.sesion02.Fraccion;

public class MatrizFraccion extends MatrizGenerica<Fraccion> {

	/**
	 * Metodo para sumar dos fracciones
	 * 
	 * @param f1 es la primera fraccion
	 * @param f2 es la segunda fraccion
	 * @return suma de primera fraccion y segunda
	 */
	@Override
	public Fraccion sumar(Fraccion f1, Fraccion f2) {
		return f1.sumar(f2);
	}

	/**
	 * Metodo para multiplicar dos fracciones
	 * 
	 * @param f1 es la primera fraccion
	 * @param f2 es la segunda fraccion
	 * @return producto de primera fraccion por segunda
	 */
	@Override
	public Fraccion multiplicar(Fraccion f1, Fraccion f2) {
		return f1.multiplicar(f2);
	}

	/**
	 * @return fraccion nula, 0/1
	 */
	@Override
	public Fraccion cero() {
		return new Fraccion();
	}

}
