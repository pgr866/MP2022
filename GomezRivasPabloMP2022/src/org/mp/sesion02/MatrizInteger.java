package org.mp.sesion02;

public class MatrizInteger extends MatrizGenerica<Integer> {

	/**
	 * Metodo para sumar dos enteros
	 * 
	 * @param o1 es primer entero
	 * @param o2 es segundo entero
	 * @return suma de primer entero y segundo
	 */
	@Override
	protected Integer sumar(Integer o1, Integer o2) {
		return new Integer(o1.intValue() + o2.intValue());
	}

	/**
	 * Metodo para multiplicar dos enteros
	 * 
	 * @param o1 es el primer entero
	 * @param o2 es el segund entero
	 * @return producto de primer entero por segundo
	 */
	@Override
	protected Integer multiplicar(Integer o1, Integer o2) {
		return new Integer(o1.intValue() * o2.intValue());
	}

	/**
	 * @return entero nulo, 0
	 */
	@Override
	protected Integer cero() {
		return new Integer(0);
	}

}
